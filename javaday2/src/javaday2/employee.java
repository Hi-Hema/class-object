package javaday2;

public class employee {

	
		int eid;
		String Username;
		String City;
		public int getEid() {
			return eid;
		}
		public void setEid(int eid) {
			this.eid = eid;
		}
		public String getUsername() {
			return Username;
		}
		public void setUsername(String username) {
			Username = username;
		}
		public String getCity() {
			return City;
		}
		public void setCity(String city) {
			City = city;
		}
		
		

	

}
